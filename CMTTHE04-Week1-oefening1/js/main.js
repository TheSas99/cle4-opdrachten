// vis element
//
function showItems(fish, bubble) {
    let fish = document.createElement("fish")
document.body.appendChild(fish)
fish.style.transform = "translate(350px, 400px)"
fish.style.filter = "hue-rotate(170deg)"

//
// bubble element
//
let bubble = document.createElement("bubble")
document.body.appendChild(bubble)
bubble.style.transform = "translate(20px, 100px)"

}


var i;
for (i = 0; i < 100; i++) {
  return showItems
}